public class OperatorPembanding {
    public static void main(String[] args) {
        int a = 12;
        int b = 4;
        boolean hasil;
        
        System.out.println(hasil=a>b);
        System.out.println(hasil=a<b);
        System.out.println(hasil=a>=b);
        System.out.println(hasil=a<=b);
        System.out.println(hasil=a==b);
        System.out.println(hasil=a!=b);
    }
}
